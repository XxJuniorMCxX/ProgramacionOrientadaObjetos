    private void cmdAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAgregarActionPerformed
        String nombre_Persona;
        String nombre_Mascota;
        String Tipo_Mascota;
        
        nombre_Persona = txtNombre.getText();
        Tipo_Mascota=txtTipoMascota.getText();
        nombre_Mascota = txtNombreMascota.getText();
        
        Mascota m = new Mascota();
        m.setNombre(nombre_Mascota);
        m.setTipo(Tipo_Mascota);
        
        Persona p = new Persona();
        p.setNombre(nombre_Mascota);
        p.setMascota(m);
        
        //List<Persona> persona= new ArrayList<>();
        persona.add(p);
        
        String datos = p.getNombre() + " Tiene Un " + p.getMascota().getTipo() + " Que Se llama " + p.getMascota().getNombre() + "\n";
        txtDatos.append(datos);
                       
    }//GEN-LAST:event_cmdAgregarActionPerformed

